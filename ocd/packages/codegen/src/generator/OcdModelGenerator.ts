    interfaceAttributeDefinition = (attribute, level: number=0) => {
        const name = attribute.name
        const propertyName = attribute.key
        const requiredString = attribute.required ? '' : '?'
        if (attribute.type === 'string')      return `${propertyName}${requiredString}: string`
        else if (attribute.type === 'bool')   return `${propertyName}${requiredString}: boolean`
        else if (attribute.type === 'number') return `${propertyName}${requiredString}: number`
        else if (attribute.type === 'object') return `${propertyName}${requiredString}: ${this.generateInterfaceReference(name, level)}`
        // else if (attribute.type === 'object') return `${key}${requiredString}: ${this.interfaceName(attribute.id)}`
        else if (attribute.type === 'list')   return `${propertyName}${requiredString}: ${attribute.subtype === 'object' ? this.generateInterfaceReference(name, level) : attribute.subtype}[]`
        // else if (attribute.type === 'list')   return `${key}${requiredString}: ${attribute.subtype === 'object' ? this.interfaceName(attribute.id) : attribute.subtype}[]`
        else if (attribute.type === 'set')    return `${propertyName}${requiredString}: ${attribute.subtype}[]`
        else if (attribute.type === 'map')    return `${propertyName}${requiredString}: {[key: string]: string}`
        else return ''
    }

    namespaceAttributeAssignment = (resource, attribute, level: number=0) => {
        const name = attribute.name
        const propertyName = attribute.key
        if (attribute.type === 'string')      return `${propertyName}: '${attribute.default ? attribute.default : ''}'`
        else if (attribute.type === 'bool')   return `${propertyName}: ${attribute.default ? attribute.default : 'false'}`
        else if (attribute.type === 'number') return `${propertyName}: ${attribute.default ? attribute.default : '0'}`
        else if (attribute.type === 'object') return `${propertyName}: ${this.generateNewFunctionCall(name, level)}`
        // else if (attribute.type === 'object') return `${key}: ${this.autoGeneratedNamespaceName(resource)}.${this.namespaceFunctionName(attribute.id)}()`
        else if (attribute.type === 'list')   return `${propertyName}: ${attribute.default ? attribute.default : '[]'}`
        else if (attribute.type === 'set')    return `${propertyName}: []`
        else if (attribute.type === 'map')    return `${propertyName}: {}`
        else return ''
    }

    autoGeneratedInterface = (i) => {
        return `
export interface ${this.interfaceName(i.id)} {
    ${Object.entries(i.attributes).filter(([k, v]) => !this.ignoreAttributes.includes(v.id)).map(([k, a]) => this.interfaceAttributeDefinition(a)).join('\n    ')}
}
`
    }

    autoGeneratedNamespaceInterface = (i) => {
        return `
    export interface ${this.interfaceName(i.id)} {
        ${Object.entries(i.attributes).filter(([k, v]) => !this.ignoreAttributes.includes(v.id)).map(([k, a]) => this.interfaceAttributeDefinition(a)).join('\n        ')}
    }`
    }

    interface = (i) => {
        return `
export interface ${this.interfaceName(i.id)} extends AutoGenerated.${this.interfaceName(i.id)} {}
`
    }

    namespaceInterface = (resource, i) => {
        return `
    export interface ${this.interfaceName(i.id)} extends AutoGenerated.${this.autoGeneratedNamespaceName(resource)}.${this.interfaceName(i.id)} {}
`
    }

    autoGeneratedNamespaceFunction = (resource, i) => {
        return `
    export function ${this.namespaceFunctionName(i.id)}(): ${this.interfaceName(i.id)} {
        return {
            ${Object.entries(i.attributes).filter(([k, v]) => !this.ignoreAttributes.includes(v.id)).map(([k, a]) => this.namespaceAttributeAssignment(resource, a)).join(',\n            ')}
        }
    }
`
    }

    namespaceFunction = (resource, i) => {
        return `
    export function ${this.namespaceFunctionName(i.id)}(): ${this.interfaceName(i.id)} {
        return {
            ...AutoGenerated.${this.autoGeneratedNamespaceName(resource)}.${this.namespaceFunctionName(i.id)}(),
        }
    }
`
    }

    // namespaceFunctionName = (name) => `new${this.interfaceName(name)}`

    outputFilename = (resource) => `${this.modelFilename(resource)}.ts`

